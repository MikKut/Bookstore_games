namespace BG.LocalApi.Domain.Enums
{
    /// <summary>
    /// Represents various genres of books.
    /// </summary>
    public enum Genre
    {
        /// <summary>
        /// Fiction genre, including any narrative that is invented or imagined.
        /// </summary>
        Fiction,

        /// <summary>
        /// Non-fiction genre, including factual works based on real events or information.
        /// </summary>
        NonFiction,

        /// <summary>
        /// Mystery genre, including works that involve solving a crime or uncovering a secret.
        /// </summary>
        Mystery,

        /// <summary>
        /// Fantasy genre, including works that involve magical or supernatural elements.
        /// </summary>
        Fantasy,

        /// <summary>
        /// Science Fiction genre, including works based on speculative science or futuristic concepts.
        /// </summary>
        ScienceFiction,

        /// <summary>
        /// Biography genre, including works that recount the life story of a person.
        /// </summary>
        Biography,

        /// <summary>
        /// Romance genre, including works that focus on romantic relationships.
        /// </summary>
        Romance,

        /// <summary>
        /// Thriller genre, including works characterized by excitement and suspense.
        /// </summary>
        Thriller,

        /// <summary>
        /// Historical genre, including works set in a specific historical period.
        /// </summary>
        Historical
    }
}
